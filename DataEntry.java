public class DataEntry {
	private int bytes;
	private String domain;

	public int getBytes() {
		return bytes;
	}

	public void setBytes(int bytes) {
		this.bytes = bytes;
	}

	public String getDomainName() {
		return domain;
	}

	public void setDomainName(String domain) {
		this.domain = domain;
	}
}
